# Write a python program to add two numbers.

# Taking the first number as input and converting it to an integer
_num1 = int(input("Enter Number one: "))

# Taking the second number as input and converting it to an integer
_num2 = int(input("Enter Number two: "))

# Adding the two numbers
_add = _num1 + _num2

# Displaying the result using an f-string
print(f"The sum of {_num1} and {_num2} is {_add}")