# Write a python program to find average of two numbers enterd by the user.

# Taking the first number as input
_num1 = int(input("Enter number one: "))

# Taking the second number as input (corrected typo)
_num2 = int(input("Enter number two: "))

# Calculating the average
_avg = (_num1 + _num2) / 2

# Displaying the result using an f-string
print(f"The average of {_num1} and {_num2} is {_avg}")