# Write a python program to calculate the square of a number entered by the user.


# Taking input from the user and converting it to an integer
_num1 = int(input("Enter the number: "))

# Calculating the square of the number using exponentiation
_square = _num1 ** 2

# Displaying the square using formatted string
print(f"The square of the given number {_num1} is {_square}")

