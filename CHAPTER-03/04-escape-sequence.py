# \n — New Line
print("Hello\nWorld")
# Output:
# Hello
# World


# \t — Tab (Horizontal Tab, like pressing the TAB key)
print("Name\tAge")
print("John\t25")
# Output:
# Name    Age
# John    25


# \' — Single Quote
print('It\'s a sunny day')
# Output: It's a sunny day


# \" — Double Quote
print("He said, \"Hello!\"")
# Output: He said, "Hello!"


# \\ — Backslash
print("C:\\Users\\Harsh")
# Output: C:\Users\Harsh

# \r — Carriage Return (moves cursor to the beginning of the line)
print("Hello\rHi")
# Output: Hi llo


# \b — Backspace (removes one character before it)
print("Helloo\b")
# Output: Hello

# \f — Form Feed (rarely used, page break in printing)
print("Hello\fWorld")
# Output may vary depending on environment


# \a — Bell (alert sound, may beep on some systems)
print("\a")  # Might make a beep sound


# \u or \U — Unicode character
print("\u2764")  # Output: ❤ (Heart symbol)


print("Name:\tHarsh\nPath:\tC:\\Users\\Harsh")
# Output:
# Name:    Harsh
# Path:    C:\Users\Harsh

