# for loop using list
_list = [22, 32.23, "Harsh", True, False, None]

# Loop through each element in the list and print it
for i in _list:
  print(i)

# for loop using tuple
_tuple = (1, 2.0, "Amit", True, False, None)

# Loop through each element in the tuple and print it
for i in _tuple:
  print(i)


# for loop using string
_string = "Harsh"

# Loop through each character in the string and print it
for i in _string:
  print(i)
