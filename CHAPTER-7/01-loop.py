# Printing numbers from 1 to 5 using individual print statements
print(1) # Output: 1
print(2) # Output: 2
print(3) # Output: 3
print(4) # Output: 4
print(5) # Output: 5

# The same task can be done more efficiently using a loop:
# Using a for loop with the range() function to print numbers from 1 to 5
for x in range(1, 6):  # range(1, 6) generates numbers from 1 to 5 (6 is excluded)
  print(x)             # print each number in the range

"""
Output: 
1
2
3
4
5
"""